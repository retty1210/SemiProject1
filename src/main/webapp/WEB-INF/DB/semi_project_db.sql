    , EMAIL VARCHAR2(50)
    , SIGNDATE date DEFAULT SYSDATE
);

ALTER TABLE SIGNUP ADD CONSTRAINT PK_PKID PRIMARY KEY(PKID);
ALTER TABLE SIGNUP MODIFY USERID CONSTRAINT NN_USERID NOT NULL;
