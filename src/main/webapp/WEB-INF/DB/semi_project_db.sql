ALTER TABLE SIGNUP MODIFY USERNAME CONSTRAINT NN_USERNAME NOT NULL;
ALTER TABLE SIGNUP MODIFY PHONENUMBER CONSTRAINT NN_PHONENUMBER NOT NULL;
